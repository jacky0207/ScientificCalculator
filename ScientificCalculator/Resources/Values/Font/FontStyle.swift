import SwiftUI

enum FontStyle: String {
    case regular = "NotoSansTC-Regular"
    case medium = "NotoSansTC-Medium"
    case bold = "NotoSansTC-Bold"
}

enum FontSize: CGFloat {
    case small = 12
    case normal = 14
    case large = 16
    case xLarge = 18
    case xxLarge = 20
}
