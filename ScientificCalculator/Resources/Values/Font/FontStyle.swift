import SwiftUI

enum FontStyle: String {
    case regular
    case medium
    case bold
}

enum FontSize: CGFloat {
    case small = 8
    case normal = 14
    case large = 18
    case xLarge = 22
    case xxLarge = 26
    case xxxLarge = 32
}
